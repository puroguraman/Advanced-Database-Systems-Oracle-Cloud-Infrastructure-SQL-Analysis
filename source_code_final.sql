/* ======================================================================
   1. ΚΑΘΑΡΙΣΜΟΣ ΒΑΣΗΣ (DROP EXISTING OBJECTS)
   ====================================================================== */
BEGIN
   -- Διαγραφή Πινάκων
   FOR cur_rec IN (SELECT table_name FROM user_tables) LOOP
      EXECUTE IMMEDIATE 'DROP TABLE "' || cur_rec.table_name || '" CASCADE CONSTRAINTS';
   END LOOP;
   
   -- Διαγραφή Διαδικασιών
   FOR cur_proc IN (SELECT object_name FROM user_procedures WHERE object_type = 'PROCEDURE') LOOP
      EXECUTE IMMEDIATE 'DROP PROCEDURE "' || cur_proc.object_name || '"';
   END LOOP;
END;
