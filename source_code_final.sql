-- 5.2. Procedure: FIND_OVERLAPPING_STUDIES
CREATE OR REPLACE PROCEDURE find_overlapping_studies (
    p_result OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
    SELECT g.last_name, g.first_name,
           d1.degree_id AS deg_1, d2.degree_id AS deg_2,
           d1.enrollment_date AS d1_start, d1.graduation_date AS d1_end,
           d2.enrollment_date AS d2_start, d2.graduation_date AS d2_end
    FROM graduate g
    JOIN degree d1 ON d1.graduate_id = g.graduate_id
    JOIN degree d2 ON d2.graduate_id = g.graduate_id
    WHERE d1.degree_id < d2.degree_id
      AND d1.enrollment_date <= NVL(d2.graduation_date, SYSDATE)
      AND d2.enrollment_date <= NVL(d1.graduation_date, SYSDATE)
    ORDER BY g.last_name;
END;
/

-- 5.3. Procedure: EMPLOYMENT_CATEGORY_SHARE
CREATE OR REPLACE PROCEDURE employment_category_share (
    p_result OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
    WITH base AS (
        SELECT d.department_name, ec.category_name, COUNT(DISTINCT w.graduate_id) AS grads_cnt
        FROM work_experience w
        JOIN employment_category ec ON ec.category_id = w.category_id
        JOIN degree deg ON deg.graduate_id = w.graduate_id
        JOIN program p ON p.program_id = deg.program_id
        JOIN department d ON d.department_id = p.department_id
        GROUP BY d.department_name, ec.category_name
    ),
    tot AS (
        SELECT department_name, SUM(grads_cnt) AS total_cnt FROM base GROUP BY department_name
    )
    SELECT b.department_name, b.category_name, b.grads_cnt,
           ROUND((b.grads_cnt / NULLIF(t.total_cnt,0)) * 100, 2) AS percentage
    FROM base b
    JOIN tot t ON t.department_name = b.department_name
    ORDER BY b.department_name, percentage DESC;
END;
/

