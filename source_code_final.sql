-- 6.5. COMPOUND TRIGGER: Μία Μόνο Ενεργή Εργασία ανά Απόφοιτο
CREATE OR REPLACE TRIGGER trg_one_active_job
FOR INSERT OR UPDATE ON work_experience
COMPOUND TRIGGER
    TYPE t_grad_ids IS TABLE OF NUMBER INDEX BY PLS_INTEGER;
    v_grads t_grad_ids;

    BEFORE EACH ROW IS
    BEGIN
        IF :NEW.end_date IS NULL THEN
            v_grads(v_grads.COUNT + 1) := :NEW.graduate_id;
        END IF;
    END BEFORE EACH ROW;

    AFTER STATEMENT IS
        v_count NUMBER;
    BEGIN
        FOR i IN 1 .. v_grads.COUNT LOOP
            SELECT COUNT(*) INTO v_count
            FROM work_experience
            WHERE graduate_id = v_grads(i)
              AND end_date IS NULL;

            IF v_count > 1 THEN
                RAISE_APPLICATION_ERROR(-20102, 'Σφάλμα: Ο απόφοιτος έχει ήδη ενεργή εργασία. Δεν επιτρέπεται δεύτερη παράλληλη απασχόληση.');
            END IF;
        END LOOP;
    END AFTER STATEMENT;
END trg_one_active_job;
/

