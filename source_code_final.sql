-- 1. FACULTY
CREATE TABLE "FACULTY" (
    "SCHOOL_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "SCHOOL_NAME" VARCHAR2(100) NOT NULL UNIQUE
);

-- 2. DEPARTMENT
CREATE TABLE "DEPARTMENT" (
    "DEPARTMENT_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "DEPARTMENT_NAME" VARCHAR2(100) NOT NULL UNIQUE,
    "SCHOOL_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_DEPARTMENT_FACULTY" FOREIGN KEY ("SCHOOL_ID") REFERENCES "FACULTY" ("SCHOOL_ID")
);

-- 3. SCIENTIFIC_FIELD
CREATE TABLE "SCIENTIFIC_FIELD" (
    "FIELD_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "FIELD_NAME" VARCHAR2(100) NOT NULL UNIQUE
);

-- 4. PROGRAM
CREATE TABLE "PROGRAM" (
    "PROGRAM_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "PROGRAM_NAME" VARCHAR2(150) NOT NULL UNIQUE,
    "PROGRAM_LEVEL" VARCHAR2(50),
    "DEPARTMENT_ID" NUMBER NOT NULL,
    "FIELD_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_PROGRAM_DEPARTMENT" FOREIGN KEY ("DEPARTMENT_ID") REFERENCES "DEPARTMENT" ("DEPARTMENT_ID"),
    CONSTRAINT "FK_PROGRAM_SCIENTIFIC_FIELD" FOREIGN KEY ("FIELD_ID") REFERENCES "SCIENTIFIC_FIELD" ("FIELD_ID")
);

-- 5. PROFESSOR
CREATE TABLE "PROFESSOR" (
    "PROFESSOR_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "LAST_NAME" VARCHAR2(50) NOT NULL,
    "FIRST_NAME" VARCHAR2(50) NOT NULL,
    "FATHER_NAME" VARCHAR2(50),
    "ID_NO" VARCHAR2(20) UNIQUE,
    "DATE_OF_BIRTH" DATE,
    "EMAIL" VARCHAR2(100) UNIQUE,
    "MOBILE_PHONE" VARCHAR2(20) UNIQUE,
    "ACADEMIC_RANK" VARCHAR2(50),
    "DEPARTMENT_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_PROFESSOR_DEPARTMENT" FOREIGN KEY ("DEPARTMENT_ID") REFERENCES "DEPARTMENT" ("DEPARTMENT_ID")
);

-- 6. GRADUATE
CREATE TABLE "GRADUATE" (
    "GRADUATE_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "REGISTRATION_NO" VARCHAR2(20) NOT NULL UNIQUE,
    "REGISTRATION_YEAR" NUMBER,
    "LAST_NAME" VARCHAR2(50) NOT NULL,
    "FIRST_NAME" VARCHAR2(50) NOT NULL,
    "FATHER_NAME" VARCHAR2(50),
    "ID_NO" VARCHAR2(20) UNIQUE,
    "DATE_OF_BIRTH" DATE,
    "EMAIL" VARCHAR2(100) NOT NULL UNIQUE,
    "LINKEDIN_URL" VARCHAR2(255) UNIQUE,
    "MOBILE_PHONE" VARCHAR2(20) UNIQUE,
    "ADDRESS" VARCHAR2(255),
    "GENDER" VARCHAR2(10)
);

-- 7. DEGREE
CREATE TABLE "DEGREE" (
    "DEGREE_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "ENROLLMENT_DATE" DATE,
    "GRADUATION_DATE" DATE,
    "GRADE" NUMBER(4,2) CHECK (GRADE BETWEEN 0 AND 10),
    "THESIS_TITLE" VARCHAR2(255),
    "THESIS_GRADE" NUMBER(4,2),
    "PROGRAM_ID" NUMBER NOT NULL,
    "GRADUATE_ID" NUMBER NOT NULL,
    "PROFESSOR_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_DEGREE_PROGRAM" FOREIGN KEY ("PROGRAM_ID") REFERENCES "PROGRAM" ("PROGRAM_ID"),
    CONSTRAINT "FK_DEGREE_GRADUATE" FOREIGN KEY ("GRADUATE_ID") REFERENCES "GRADUATE" ("GRADUATE_ID") ON DELETE CASCADE,
    CONSTRAINT "FK_DEGREE_PROFESSOR" FOREIGN KEY ("PROFESSOR_ID") REFERENCES "PROFESSOR" ("PROFESSOR_ID")
);

-- 8. COMPANY
CREATE TABLE "COMPANY" (
    "COMPANY_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "COMPANY_NAME" VARCHAR2(150) NOT NULL UNIQUE,
    "BUSINESS_SECTOR" VARCHAR2(100),
    "PHONE" VARCHAR2(20),
    "EMAIL" VARCHAR2(100) UNIQUE,
    "ADDRESS" VARCHAR2(255),
    "WEBSITE" VARCHAR2(255) UNIQUE,
    "IS_VERIFIED" CHAR(1) DEFAULT 'N' CHECK (IS_VERIFIED IN ('Y','N'))
);

-- 9. EMPLOYMENT_CATEGORY
CREATE TABLE "EMPLOYMENT_CATEGORY" (
    "CATEGORY_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "CATEGORY_NAME" VARCHAR2(100) NOT NULL UNIQUE
);

-- 10. JOB_LEVEL
CREATE TABLE "JOB_LEVEL" (
    "JOB_LEVEL_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "JOB_LEVEL_TITLE" VARCHAR2(50) NOT NULL UNIQUE,
    "DESCRIPTION" CLOB
);

-- 11. RELEVANCE_LEVEL
CREATE TABLE "RELEVANCE_LEVEL" (
    "RELEVANCE_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "RELEVANCE_DEGREE" VARCHAR2(50) NOT NULL UNIQUE
);

-- 12. WORK_EXPERIENCE
CREATE TABLE "WORK_EXPERIENCE" (
    "WORK_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "JOB_TITLE" VARCHAR2(100),
    "START_DATE" DATE NOT NULL,
    "END_DATE" DATE,
    "WORK_TYPE" VARCHAR2(50) CHECK (WORK_TYPE IN ('Full-time', 'Part-time', 'Contract', 'Freelance', 'Seasonal')),
    "GRADUATE_ID" NUMBER NOT NULL,
    "COMPANY_ID" NUMBER NOT NULL,
    "CATEGORY_ID" NUMBER NOT NULL,
    "JOB_LEVEL_ID" NUMBER NOT NULL,
    "RELEVANCE_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_WORK_GRAD" FOREIGN KEY ("GRADUATE_ID") REFERENCES "GRADUATE" ("GRADUATE_ID") ON DELETE CASCADE,
    CONSTRAINT "FK_WORK_COMP" FOREIGN KEY ("COMPANY_ID") REFERENCES "COMPANY" ("COMPANY_ID"),
    CONSTRAINT "FK_WORK_CAT" FOREIGN KEY ("CATEGORY_ID") REFERENCES "EMPLOYMENT_CATEGORY" ("CATEGORY_ID"),
    CONSTRAINT "FK_WORK_JOB_LVL" FOREIGN KEY ("JOB_LEVEL_ID") REFERENCES "JOB_LEVEL" ("JOB_LEVEL_ID"),
    CONSTRAINT "FK_WORK_REL" FOREIGN KEY ("RELEVANCE_ID") REFERENCES "RELEVANCE_LEVEL" ("RELEVANCE_ID"),
    CONSTRAINT "CHK_WORK_PERIOD" CHECK (END_DATE >= START_DATE OR END_DATE IS NULL)
);

-- 13. FEEDBACK
CREATE TABLE "FEEDBACK" (
    "FEEDBACK_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "FEEDBACK_DATE" DATE DEFAULT CURRENT_DATE,
    "EVALUATOR_ROLE" VARCHAR2(50),
    "COMMENTS" CLOB,
    "WORK_ID" NUMBER NOT NULL,
    "COMPANY_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_FEEDBACK_WORK" FOREIGN KEY ("WORK_ID") REFERENCES "WORK_EXPERIENCE" ("WORK_ID") ON DELETE CASCADE,
    CONSTRAINT "FK_FEEDBACK_COMPANY" FOREIGN KEY ("COMPANY_ID") REFERENCES "COMPANY" ("COMPANY_ID")
);

-- 14. SKILLS
CREATE TABLE "SKILLS" (
    "SKILL_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "SKILL_NAME" VARCHAR2(100) NOT NULL UNIQUE,
    "CATEGORY" VARCHAR2(50)
);

-- 15. RATING
CREATE TABLE "RATING" (
    "RATING_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "SCORE" NUMBER NOT NULL CHECK (SCORE BETWEEN 1 AND 10),
    "IMPORTANCE" VARCHAR2(50),
    "FEEDBACK_ID" NUMBER NOT NULL,
    "SKILL_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_RATING_FEEDBACK" FOREIGN KEY ("FEEDBACK_ID") REFERENCES "FEEDBACK" ("FEEDBACK_ID") ON DELETE CASCADE,
    CONSTRAINT "FK_RATING_SKILL" FOREIGN KEY ("SKILL_ID") REFERENCES "SKILLS" ("SKILL_ID")
);

-- 16. RECOMMENDATION
CREATE TABLE "RECOMMENDATION" (
    "RECOMMENDATION_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "RECOMMENDATION_DATE" DATE,
    "FILE_PATH" VARCHAR2(255) NOT NULL UNIQUE,
    "PROFESSOR_ID" NUMBER NOT NULL,
    "GRADUATE_ID" NUMBER NOT NULL,
    "COMPANY_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_REC_PROF" FOREIGN KEY ("PROFESSOR_ID") REFERENCES "PROFESSOR" ("PROFESSOR_ID"),
    CONSTRAINT "FK_REC_GRAD" FOREIGN KEY ("GRADUATE_ID") REFERENCES "GRADUATE" ("GRADUATE_ID") ON DELETE CASCADE,
    CONSTRAINT "FK_REC_COMP" FOREIGN KEY ("COMPANY_ID") REFERENCES "COMPANY" ("COMPANY_ID")
);

-- 17. EXTERNAL_INSTITUTION
CREATE TABLE "EXTERNAL_INSTITUTION" (
    "INSTITUTION_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "INSTITUTION_NAME" VARCHAR2(150) NOT NULL UNIQUE,
    "INSTITUTION_TYPE" VARCHAR2(50),
    "COUNTRY" VARCHAR2(50),
    "WEBSITE" VARCHAR2(255)
);

-- 18. EXTERNAL_DEGREE
CREATE TABLE "EXTERNAL_DEGREE" (
    "EX_DEGREE_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    "DEGREE_TITLE" VARCHAR2(150),
    "DEGREE_LEVEL" VARCHAR2(50),
    "ENROLLMENT_DATE" DATE,
    "GRADUATION_DATE" DATE,
    "GRADE" NUMBER(4,2),
    "INSTITUTION_ID" NUMBER NOT NULL,
    "FIELD_ID" NUMBER NOT NULL,
    "GRADUATE_ID" NUMBER NOT NULL,
    CONSTRAINT "FK_EXDEG_INST" FOREIGN KEY ("INSTITUTION_ID") REFERENCES "EXTERNAL_INSTITUTION" ("INSTITUTION_ID"),
    CONSTRAINT "FK_EXDEG_FIELD" FOREIGN KEY ("FIELD_ID") REFERENCES "SCIENTIFIC_FIELD" ("FIELD_ID"),
    CONSTRAINT "FK_EXDEG_GRAD" FOREIGN KEY ("GRADUATE_ID") REFERENCES "GRADUATE" ("GRADUATE_ID") ON DELETE CASCADE
);

