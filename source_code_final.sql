-- 5.1. Procedure: FIND_CAREER_GAPS 
CREATE OR REPLACE PROCEDURE find_career_gaps (
  p_threshold_months IN NUMBER,
  p_result OUT SYS_REFCURSOR
) AS
BEGIN
  OPEN p_result FOR
  WITH ordered_jobs AS (
    SELECT w.*, LAG(w.end_date) OVER (PARTITION BY w.graduate_id ORDER BY w.start_date) AS prev_end_date
      FROM work_experience w
  ),
  gaps_between AS (
    SELECT g.last_name, g.first_name, g.registration_no, 'Between Jobs' AS gap_type,
           oj.prev_end_date AS gap_start, oj.start_date AS gap_end,
           FLOOR(MONTHS_BETWEEN(oj.start_date, oj.prev_end_date)) AS gap_months
      FROM ordered_jobs oj
      JOIN graduate g ON g.graduate_id = oj.graduate_id
     WHERE oj.prev_end_date IS NOT NULL
       AND oj.start_date > oj.prev_end_date
       AND FLOOR(MONTHS_BETWEEN(oj.start_date, oj.prev_end_date)) > p_threshold_months
  ),
  first_job AS (
    SELECT graduate_id, MIN(start_date) AS first_start FROM work_experience GROUP BY graduate_id
  ),
  grad_date AS (
    SELECT graduate_id, MAX(graduation_date) AS graduation_date FROM degree GROUP BY graduate_id
  ),
  gaps_degree_first AS (
    SELECT g.last_name, g.first_name, g.registration_no, 'Degree to First Job' AS gap_type,
           gd.graduation_date AS gap_start, fj.first_start AS gap_end,
           FLOOR(MONTHS_BETWEEN(fj.first_start, gd.graduation_date)) AS gap_months
      FROM graduate g
      JOIN grad_date gd ON gd.graduate_id = g.graduate_id
      JOIN first_job fj ON fj.graduate_id = g.graduate_id
     WHERE gd.graduation_date IS NOT NULL
       AND fj.first_start > gd.graduation_date
       AND FLOOR(MONTHS_BETWEEN(fj.first_start, gd.graduation_date)) > p_threshold_months
  ),
  last_job AS (
    SELECT graduate_id, MAX(end_date) AS last_end FROM work_experience GROUP BY graduate_id
  ),
  has_active AS (
    SELECT DISTINCT graduate_id FROM work_experience WHERE end_date IS NULL
  ),
  gaps_current AS (
    SELECT g.last_name, g.first_name, g.registration_no, 'Current Unemployment' AS gap_type,
           lj.last_end AS gap_start, TRUNC(SYSDATE) AS gap_end,
           FLOOR(MONTHS_BETWEEN(TRUNC(SYSDATE), lj.last_end)) AS gap_months
      FROM graduate g
      JOIN last_job lj ON lj.graduate_id = g.graduate_id
      LEFT JOIN has_active ha ON ha.graduate_id = g.graduate_id
     WHERE lj.last_end IS NOT NULL
       AND lj.last_end < TRUNC(SYSDATE)
       AND ha.graduate_id IS NULL
       AND FLOOR(MONTHS_BETWEEN(TRUNC(SYSDATE), lj.last_end)) > p_threshold_months
  ),
  no_work_yet AS (
    SELECT g.last_name, g.first_name, g.registration_no, 'No Work Yet' AS gap_type,
           gd.graduation_date AS gap_start, TRUNC(SYSDATE) AS gap_end,
           FLOOR(MONTHS_BETWEEN(TRUNC(SYSDATE), gd.graduation_date)) AS gap_months
      FROM graduate g
      JOIN grad_date gd ON gd.graduate_id = g.graduate_id
      LEFT JOIN first_job fj ON fj.graduate_id = g.graduate_id
     WHERE fj.graduate_id IS NULL
       AND gd.graduation_date IS NOT NULL
       AND FLOOR(MONTHS_BETWEEN(TRUNC(SYSDATE), gd.graduation_date)) > p_threshold_months
  )
  SELECT * FROM gaps_between
  UNION ALL
  SELECT * FROM gaps_degree_first
  UNION ALL
  SELECT * FROM gaps_current
  UNION ALL
  SELECT * FROM no_work_yet
  ORDER BY last_name, first_name, gap_type, gap_months DESC;
END;
