--  DDL for Procedure EMPLOYMENT_CATEGORY_SHARE
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "EMPLOYMENT_CATEGORY_SHARE" ( 
    p_result OUT SYS_REFCURSOR
) AS
BEGIN 
    OPEN p_result FOR 
    WITH base AS ( 
        SELECT d.department_name, ec.category_name, COUNT(DISTINCT w.graduate_id) AS grads_cnt 
        FROM work_experience w 
        JOIN employment_category ec ON ec.category_id = w.category_id 
        JOIN degree deg ON deg.graduate_id = w.graduate_id 
        JOIN program p ON p.program_id = deg.program_id 
        JOIN department d ON d.department_id = p.department_id 
        GROUP BY d.department_name, ec.category_name 
    ), 
    tot AS ( 
        SELECT department_name, SUM(grads_cnt) AS total_cnt FROM base GROUP BY department_name 
    ) 
    SELECT b.department_name, b.category_name, b.grads_cnt, 
           ROUND((b.grads_cnt / NULLIF(t.total_cnt,0)) * 100, 2) AS percentage 
    FROM base b 
    JOIN tot t ON t.department_name = b.department_name 
    ORDER BY b.department_name, percentage DESC;
END;


