  CREATE OR REPLACE EDITIONABLE PROCEDURE "FIND_OVERLAPPING_STUDIES" ( 
    p_result OUT SYS_REFCURSOR
) AS
BEGIN 
    OPEN p_result FOR 
    SELECT g.last_name, g.first_name, d1.degree_id AS deg_1, d2.degree_id AS deg_2,
           d1.enrollment_date AS d1_start, d1.graduation_date AS d1_end, 
           d2.enrollment_date AS d2_start, d2.graduation_date AS d2_end 
    FROM graduate g 
    JOIN degree d1 ON d1.graduate_id = g.graduate_id 
    JOIN degree d2 ON d2.graduate_id = g.graduate_id 
    WHERE d1.degree_id < d2.degree_id 
      AND d1.enrollment_date <= NVL(d2.graduation_date, SYSDATE) 
      AND d2.enrollment_date <= NVL(d1.graduation_date, SYSDATE) 
    ORDER BY g.last_name;
END;


